    public void InitDataController()
    {
        projectController = new Project_Controller();
        taskController = new Task_Controller();
    }
    
    public void InitComponentModel()
    {
        projectsModel = new DefaultListModel();
        LoadProjects();
        
        taskModel = new TaskTableModel();
        
        Task_TableTasks.setModel(taskModel);
        Task_TableTasks.getColumnModel().getColumn(2).setCellRenderer(new DeadLineColor());
        Task_TableTasks.getColumnModel().getColumn(4).setCellRenderer(new EditButton("Edit"));
        Task_TableTasks.getColumnModel().getColumn(5).setCellRenderer(new EditButton("Delete"));

        if (!projectsModel.isEmpty()) {
            Project_Lists.setSelectedIndex(0);
            int projectIndex = Project_Lists.getSelectedIndex();
            Project project = (Project) projectsModel.get(projectIndex);
            LoadTasks(project.getId());
        }
    }
    
    public void LoadTasks(int id)
    {
        List<Task> task = taskController.GetAll(id);
        taskModel.setTasks(task);
        ShowTableTasks(!task.isEmpty());
    }
    
    public void LoadProjects()
    {
        List<Project> projects = projectController.GetAll();
        projectsModel.clear();
        if(projects.isEmpty())
        {
            ShowTableTasks(false);
            return;
        }
        for(int x = 0; x < projects.size(); x++)
        {
            Project project = projects.get(x);
            projectsModel.addElement(project);
        }
        Project_Lists.setModel(projectsModel);
    }
    
    private void ShowTableTasks(boolean hasTasks)
    {
        if(hasTasks)
        {
            if(Panel_EmptyList.isVisible())
            {
                Panel_EmptyList.setVisible(false);
                jPanel5.remove(Panel_EmptyList);
            }
            jPanel5.add(ScrollPanel_Task);
            ScrollPanel_Task.setVisible(true);
            ScrollPanel_Task.setSize(jPanel5.getWidth(), jPanel5.getHeight());
        }
        else
        {
            if(ScrollPanel_Task.isVisible())
            {
                ScrollPanel_Task.setVisible(false);
                jPanel5.remove(ScrollPanel_Task);
            }
            jPanel5.add(Panel_EmptyList);
            Panel_EmptyList.setVisible(true);
            Panel_EmptyList.setSize(jPanel5.getWidth(), jPanel5.getHeight());
        }
    }

