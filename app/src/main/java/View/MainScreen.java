    private void Tasks_AddTaskMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tasks_AddTaskMouseClicked
        
        if(projectsModel.isEmpty())
        {
            JOptionPane.showMessageDialog(rootPane, "Por favor crie um projeto");
            return;
        }
        TaskDialogScreen dialog = new TaskDialogScreen(this, rootPaneCheckingEnabled);
        int projectIndex = Project_Lists.getSelectedIndex();
        Project project = (Project)projectsModel.get(projectIndex);
        
        dialog.setProject(project);
        dialog.setLocationRelativeTo(null);
        dialog.setVisible(true);
        
        dialog.addWindowListener(new WindowAdapter()
        {
            public void windowClosed(WindowEvent e)
            {
                int projectIndex = Project_Lists.getSelectedIndex();
                Project project = (Project)projectsModel.get(projectIndex);
                LoadTasks(project.getId());
            }
        });
    }//GEN-LAST:event_Tasks_AddTaskMouseClicked

    private void Task_TableTasksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Task_TableTasksMouseClicked
        int rowIndex = Task_TableTasks.rowAtPoint(evt.getPoint());
        int columnIndex = Task_TableTasks.columnAtPoint(evt.getPoint());
        Task task = taskModel.getTasks().get(rowIndex);
        int projectIndex = Project_Lists.getSelectedIndex();
        Project project = (Project)projectsModel.get(projectIndex);
        switch(columnIndex)
        {
            case 3:
                taskController.Update(task);
                
                break;
            case 4:
                TaskDialogScreen_Update update = new TaskDialogScreen_Update(this,rootPaneCheckingEnabled);
                update.setProject(project);
                update.GetValuesProject(task);
                update.setLocationRelativeTo(null);
                update.setVisible(true);
                update.addWindowListener(new WindowAdapter()
                {
                    public void windowClosed(WindowEvent e)
                    {
                        int projectIndex = Project_Lists.getSelectedIndex();
                        Project project = (Project)projectsModel.get(projectIndex);
                        update.AtualizarDados(task);
                        taskController.Update(task);
                        LoadTasks(project.getId());
                    }
                });
                break;

            case 5:
                int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir a tarefa?", 
                        "Confirmação", JOptionPane.YES_NO_OPTION);
                if(resposta == JOptionPane.YES_OPTION)
                {
                    taskController.RemoveById(task.getId());
                    taskModel.getTasks().remove(task);
                }
                else
                {
                    return;
                }
                LoadTasks(project.getId());
                break;
        }
        
    }//GEN-LAST:event_Task_TableTasksMouseClicked

    private void Project_ListsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Project_ListsMouseClicked
        int projectIndex = Project_Lists.getSelectedIndex();
        Project project = (Project)projectsModel.get(projectIndex);
        LoadTasks(project.getId());
        if(SwingUtilities.isRightMouseButton(evt))
        {
            int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o projeto selecionado?\n                       '" +project.getName() + "'", 
                        "Confirmação", JOptionPane.YES_NO_OPTION);
            if(resposta == JOptionPane.YES_OPTION)
            {
                projectController.RemoveById(project.getId());
                JOptionPane.showMessageDialog(rootPane, "Projeto excluído com sucesso");
            }
            else
            {
                return;
            }
            LoadProjects();
        }
    }//GEN-LAST:event_Project_ListsMouseClicked

